def irv_to_stv_ballot(ballots, num_winners):
    '''(list of lists, int) -> list of lists
    >>> irv_to_stv_ballot([['NDP', 'CPC'], ['GREEN']], 3)
    [['NDP0', 'NDP1', 'NDP2', 'CPC0', 'CPC1', 'CPC2'], ['GREEN0', 'GREEN1', 'GREEN2']]
    '''
    # creates a new list
    new_list = []

    for element in ballots:
        
        # creates a sublist for every element in ballots
        sub_list = []
        # adds "num_winners" of times elem in sub_list with a number
        for elem in element:
            for i in range(num_winners):
                candidate = elem + str(i)
                sub_list.append(candidate)
                
        # adds both lists together for every element
        new_list.append(sub_list)

    # returns the list
    return new_list
            


